    private void bt_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_deleteActionPerformed
        CarDAO dao = new CarsDAOImp();
        Object rowIndex = tbl_cars.getSelectedRow();
        int confirm = JOptionPane.showConfirmDialog(this, "Doyou want to delete this  item ?");
        if (confirm == 0) {
            int del = dao.deleteCarById(carsList.get((Integer) rowIndex).getId());
            initTable();
        }
    }//GEN-LAST:event_bt_deleteActionPerformed


