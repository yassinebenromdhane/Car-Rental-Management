    private void tbl_carsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_carsMouseClicked
        int row = tbl_cars.rowAtPoint(evt.getPoint());

        if (evt.getClickCount() == 2) {
            Object id = tbl_cars.getModel().getValueAt(row, 0);
            Object ob = tbl_cars.getModel().getValueAt(row, 1);
            System.out.println(id);
            System.out.println(ob);
            CarDialog st = new CarDialog(frame, true, true, (Integer) id, tbl_cars, carsList ,0);
            st.setVisible(true);
            // open editor
        }
    }//GEN-LAST:event_tbl_carsMouseClicked

