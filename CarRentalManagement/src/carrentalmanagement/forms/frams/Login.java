        Admin admin = imp.login(tf_email.getText().trim(), tf_pass.getText().trim());
        if (admin.getId()>0){
             Home home=new Home();
             home.setVisible(true);
             this.dispose();
        }
        else{
            JOptionPane.showMessageDialog(this,"Invalid Identifier");
        }
